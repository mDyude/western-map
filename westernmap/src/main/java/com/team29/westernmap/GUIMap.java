    public void displayWeather() throws IOException, InterruptedException, ParseException{
        Weather weather = new Weather();
        JSONObject info = (JSONObject) weather.getWeather();
        String location = (String) info.get("address");
        JSONArray days = (JSONArray) info.get("days");
        JSONObject days2 = (JSONObject) days.get(0);
        double tempmin = (double) days2.get("tempmin");
        String datetime = (String) days2.get("datetime");
        double feelslike = (double) days2.get("feelslike");
        double tempmax = (double) days2.get("tempmax");
        double temp = (double) days2.get("temp");
        
        loctable2.setText(location);
        datelabel.setText(datetime);
        mintemplabel.setText(tempmin + " 째C");
        maxtemplabel.setText(tempmax + " 째C");
        feelslabel.setText(feelslike + " 째C");
        templabel.setText(temp + " 째C");
    }
    
